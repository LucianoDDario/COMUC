CREATE TABLE aluno (
    id_aluno integer GENERATED BY DEFAULT AS IDENTITY,
    nome character varying(50) NOT NULL,
    data_de_nascimento timestamp without time zone NOT NULL,
    telefone character varying(11) NOT NULL,
    cpf character varying(11) NOT NULL,
    rg character varying(9) NOT NULL,
    endereco character varying(200) NOT NULL,
    nome_do_pai character varying(50) NOT NULL,
    nome_da_mae character varying(50) NOT NULL,
    bolsista boolean NOT NULL,
    data_de_inicio timestamp without time zone,
    motivo_de_saida character varying(100) NOT NULL,
    possui_instrumento_proprio boolean NOT NULL,
    tamanho_da_vestimenta character varying(50) NOT NULL,
    CONSTRAINT "PK_aluno" PRIMARY KEY (id_aluno)
);

CREATE TABLE funcionario (
    id_funcionario integer GENERATED BY DEFAULT AS IDENTITY,
    nome character varying(50) NOT NULL,
    senha character varying(255) NOT NULL,
