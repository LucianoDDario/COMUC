);

CREATE TABLE professor (
    id_professor integer GENERATED BY DEFAULT AS IDENTITY,
    nome character varying(50) NOT NULL,
    senha character varying(255) NOT NULL,
    CONSTRAINT "PK_professor" PRIMARY KEY (id_professor)
);

CREATE TABLE banda (
    id_banda integer GENERATED BY DEFAULT AS IDENTITY,
    nome text NOT NULL,
    id_professor integer NOT NULL,
    banda_pai_id integer,
    CONSTRAINT "PK_banda" PRIMARY KEY (id_banda),
    CONSTRAINT "FK_banda_banda_banda_pai_id" FOREIGN KEY (banda_pai_id) REFERENCES banda (id_banda),
    CONSTRAINT "FK_banda_professor_id_professor" FOREIGN KEY (id_professor) REFERENCES professor (id_professor) ON DELETE CASCADE
);

CREATE TABLE nota (
