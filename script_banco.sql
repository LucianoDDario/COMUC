    descricao text NOT NULL,
    id_aluno integer NOT NULL,
    id_professor integer NOT NULL,
    CONSTRAINT "PK_nota" PRIMARY KEY (id_nota),
    CONSTRAINT "FK_nota_aluno_id_aluno" FOREIGN KEY (id_aluno) REFERENCES aluno (id_aluno) ON DELETE CASCADE,
    CONSTRAINT "FK_nota_professor_id_professor" FOREIGN KEY (id_professor) REFERENCES professor (id_professor) ON DELETE CASCADE
);

CREATE TABLE banda_aluno (
